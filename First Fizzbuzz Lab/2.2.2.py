# Set the limit to 12.
# This means the program will count up until the counter is no longer less than 12.
limit = 12

# Set the starting value of the counter to 1.
counter = 1

# Keep repeating this loop while counter is less than limit.
while counter < limit:
    # Print the current value of counter.
    print(counter)

    # Add 1 to counter after printing it.
    counter = counter + 1
